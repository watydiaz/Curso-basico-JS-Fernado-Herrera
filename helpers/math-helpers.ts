//funcion para agrgar un numero 
export function addTwoNumbers (num1: number, num2: number){ 
    return num1 + num2;
}
//restar
export function substractTwonNumber (num1: number, num2: number){ 
    return num1 - num2;
}
//multiplicar
export function timesTwoNumbers (num1: number, num2: number){ 
    return num1 * num2;
}
//Dividir
export function divideTwoNumers (num1: number, num2: number){ 
    return num1 / num2;
}